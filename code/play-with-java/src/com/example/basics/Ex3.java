package com.example.basics;

public class Ex3 {
    public static void main(String[] args) {

        // Looping statements
        /*
                - for
                - while
                - do-while

         */
        //---------------------------------------

        for(int i=0;i<5;i++){
            System.out.println("hello "+i);
        }

        //---------------------------------------

        int i=0;
        while(i<5){
            System.out.println("hi "+i);
            i++;
        }

        //---------------------------------------

        int j=0;
        do{
            System.out.println("hola "+i);
            i++;
        }while(i<5);

        //---------------------------------------

    }
}
